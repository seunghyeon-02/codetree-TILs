l = k.split()

a = int(l[0])
b = int(l[1])

if a >= b:
    print(1)
else:
    print(0)

if a > b:
    print(1)
else:
    print(0)

if b >= a:
    print(1)
else:
    print(0)

if b > a:
    print(1)
else:
    print(0)

if a == b:
    print(1)
else:
    print(0)
    
